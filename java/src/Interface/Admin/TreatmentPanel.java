    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = treatmentCatalogueTable.getSelectedRow();
        
       if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select Treatmen to add" ,"Warning" , JOptionPane.WARNING_MESSAGE);
            return;
        } 
       
       String treatmentName =  treatmentCatalogueTable.getValueAt(selectedRow, 0).toString();
       String treatmentCost    =  treatmentCatalogueTable.getValueAt(selectedRow, 1).toString();
       
       DefaultTableModel model = (DefaultTableModel) treatmentTable.getModel();
        try {
            statement.executeUpdate("INSERT INTO treatment VALUES ('" 
                    + treatmentCost +"','" + appointmentId + "','"+ treatmentName + "')");
             model.addRow(new Object[]
       { treatmentName , 
         treatmentCost  });
       
       JOptionPane.showMessageDialog(null, "Treatment Details Added");
        } catch (SQLException ex) {
            Logger.getLogger(TreatmentPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    
      
     
    }//GEN-LAST:event_addButtonActionPerformed

