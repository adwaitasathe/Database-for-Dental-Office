    private void addToothActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToothActionPerformed
        try {
            // TODO add your handling code here:
            String toothType = toothTypeCombobox.getSelectedItem().toString();
            String toothID = toothComboBox.getSelectedItem().toString();
            String toothTreatment = toothTreatmentField.getText();
            
            
            CallableStatement stmt = null;
            
            stmt = connection.prepareCall("{call insertToothRow(?,?,?,?)}");
            
            stmt.setString(1, appointmentId);
            stmt.setString(2, toothID);
            stmt.setString(3, toothType);
            stmt.setString(4, toothTreatment);            
            
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Tooth Details Added");
        } catch (SQLException ex) {
            Logger.getLogger(TreatmentPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addToothActionPerformed

